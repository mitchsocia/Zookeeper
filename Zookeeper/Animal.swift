import Foundation

class Animal {
    
    var species: String
    var gender: String
    var name: String
    var habitat: String
    
    init(species: String, gender: String, name: String, habitat: String) {
        self.species = species
        self.gender = gender
        self.name = name
        self.habitat = habitat
        
    }
    
    
}
